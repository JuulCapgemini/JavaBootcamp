package capgemini.academy.bootcamp.movies.models;

public class Movie {
    private String name;
    private String id;

    public Movie() {

    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setId(String id) {
        this.id = id;
    }
}
